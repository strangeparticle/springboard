package com.strangeparticle.springboard.app.ui.theme.branding

/**
 * Central set of icon keys used throughout the app.
 * Each key maps to a logical icon role rather than a specific icon resource,
 * allowing brands to override individual icons.
 */
enum class UiBrandIconKey {
    APP_LOGO,
    BACK_NAVIGATION,
    SETTINGS,
    RELOAD,
    COPY,
    COPY_CONFIRMED,
    DISMISS,
    SEVERITY_ERROR,
    SEVERITY_WARNING,
    SEVERITY_INFO,
}
